# 그래프 노드와 간선으로 이루어진 자료구조
# 차수: 간선으로 연결된 정점의 갯수
# 간선의 방향이 있냐 없냐에 따라 무방향그래프, 방향그래프로 나뉨
# indegree : 진입차수 outdegree: 진출차수 (방향그래프에서만존재)
# 특정 노드 위치에서 시작해 자기자신으로 돌아올수있는 경로 = 사이클
# 사이클 존재 유무에 따라 순환그래프, 비순환 그래프로 나뉨
# 완전그래프: 간선과 노드가 모두연결되어있는그래프
# 연결그래프: 두 정점사이에 항상 경로가 존재하는 그래프

# bfs: 그래프에서 인접노드를 기준으로 탐색수행
# dfs: 그래프에서 깊이가 깊은 노드를 기준으로 탐색 수행

